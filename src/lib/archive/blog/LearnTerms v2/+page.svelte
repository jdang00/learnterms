				<h3 class="mb-4 mt-8 text-2xl font-semibold">Overview of the Exam Module</h3>
				<p class="mb-8 text-lg leading-relaxed">
					The <strong>Exam Module</strong> is designed to replicate the experience of ExamSoft while
