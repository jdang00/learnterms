<h1>Getting started</h1>

<p>This quick guide helps you set up LearnTerms.</p>

<h2>Prerequisites</h2>
<ul>
	<li>Clerk account configured</li>
	<li>Convex backend set</li>
	<li>At least one cohort to join</li>
</ul>

<h2>Steps</h2>
<ol>
	<li>Sign up and join your cohort.</li>
	<li>Create a class and add modules.</li>
	<li>Add documents in Content Library and generate chunks.</li>
	<li>Assign modules and practice questions.</li>
</ol>
