<h1>Onboarding</h1>

<p><strong>LearnTerms is a collaborative, personal, and focused study platform built around the way you and your class actually learn.</strong></p>

<p>At its core, LearnTerms is three things:</p>

<ol>
	<li><strong>A shared study space for your cohort.</strong> Log in and access questions created by designated contributors in your class. These are curated, high-yield reviews designed to help everyone prepare for quizzes and exams. Together.</li>
	<li><strong>An AI-powered question creation engine.</strong> Our custom-built AI is designed specifically for generating relevant, exam-style questions. We’re not a note summarizer or a generic chatbot. Instead, LearnTerms helps you scaffold, refine, and expand your own questions, so you spend less time writing and more time studying.</li>
	<li><strong>A personal question bank that grows into a boards resource.</strong> By organizing questions into chapters, modules, and topics, LearnTerms naturally builds a structured, searchable question bank. When boards season comes, you’ll already have thousands of questions across multiple subjects at your fingertips, ready for full-scale review.</li>
</ol>

<h2>Getting Your Cohort Started</h2>
<p>LearnTerms connects you with your class’s shared study materials. To join, you’ll need your cohort’s unique join code.</p>

<ol>
	<li>Go to the <a href="https://learnterms.com/join-class">Join Class</a> page.</li>
	<li>Enter your code and review the class details.</li>
	<li>Click <strong>Confirm and Join.</strong></li>
</ol>

<p>That's it! You'll now see your cohort's classes, modules, and questions, nearly organized by semester.</p>

<h2>Navigating the Dashboard</h2>

<h3>Profile &amp; Badges</h3>
<ul>
	<li>Your name and photo</li>
	<li>Your school and graduation year</li>
	<li>Badges for your role (e.g., Admin, Contributor)</li>
</ul>

<p>This makes it easy to know what permissions you have in the platform.</p>

<h3>My Classes</h3>
<p>The main section lists all of your current classes. Each class card shows:</p>
<ul>
	<li>The class title and short description</li>
	<li>A tag for the subject (e.g., <em>Pharmacology</em>, <em>Pathology</em>)</li>
	<li>A button to enter the class</li>
</ul>

<p>Clicking into a class takes you to its <strong>modules</strong>.</p>

<h3>Modules</h3>
<p>Inside a class, you’ll see a breakdown of modules (e.g., <em>Beta Blockers</em>, <em>Steroids</em>, <em>Autoimmune Diseases</em>). Each module card displays:</p>
<ul>
	<li>The module name and description</li>
	<li>How many questions it contains (and how many you’ve completed)</li>
	<li>A quick link to start studying</li>
</ul>

<p>This helps you track progress section by section.</p>

<h3>Quick Actions</h3>
<p>On the right-hand side, you’ll find <strong>Quick Actions</strong> like the <em>Admin Dashboard</em>.</p>
<ul>
	<li>Regular students will mostly use classes and modules.</li>
	<li>Admins and contributors will see extra tools here for managing content.</li>
</ul>

<h3>Studying a Module</h3>
<p>When you open a module, you’ll enter the question view. From here you can:</p>
<ul>
	<li>See your progress at the top (e.g., “13% done”)</li>
	<li>Work through multiple-choice questions one at a time</li>
	<li>Reveal solutions when you’re ready</li>
	<li>Navigate between questions using the top bar</li>
</ul>

<p>This flow keeps your focus on one question at a time while showing your overall progress.</p>

<p>✅ <strong>Tip:</strong> The dashboard is designed to keep everything organized by semester → class → module → questions. If you ever get lost, just return to the dashboard to reset.</p>


